          <p>O vazamento de petróleo no mar causa muitos problemas para os animais e para as pessoas. As aves marinhas são muito afetadas: quando mergulham na água, suas penas ficam cobertas de óleo. Isso faz com que elas percam o isolamento térmico, ou seja, elas não conseguem manter a temperatura do corpo e podem acabar morrendo de frio. Além disso, elas não conseguem mais voar nem procurar alimento direito.</p>
          <p>Peixes, tartarugas e outros animais marinhos também sofrem muito. O petróleo pode entrar em suas brânquias (como se fossem os "pulmões" dos peixes) e dificultar a respiração. As tartarugas podem confundir o óleo com alimento e engolir, o que faz muito mal para o seu corpo. Alguns animais ficam presos nas manchas de óleo e não conseguem escapar.</p>
          <p>É muito difícil limpar o petróleo do mar, porque ele se espalha com a ajuda das ondas, das correntes e do vento.</p>
          <p>Mesmo sendo muito usado para fazer gasolina, energia e objetos de plástico, o petróleo pode ser perigoso para o meio ambiente, especialmente quando não é cuidado do jeito certo.</p>
